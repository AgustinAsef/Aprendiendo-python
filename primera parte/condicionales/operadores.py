#operadores de comparacion
x = 0

y = 0
if x == y: #verifica que las variables recibidas sean iguales
  print('condicional con comparacion')
  
if x != y: #verifica que las variables recibidas sean diferentes
  print('condicional con comparacion')

if x >= y: #verifica que x sea mayor o igual a y
  print('condicional con comparacion')
  
if x <= y: #verifica que y sea mayor o igual a x
  print('condicional con comparacion')
  
if x < y: #verifica que x sea menor a y
  print('condicional con comparacion')
  
if x > y: #verifica que x sea mayor a y
  print('condicional con comparacion')
  

#operadores logicos
if x == y & x > y : #verifica que ambas condiciones se cumplan para ejecutar el codigo
  print('operadores logicos ')

if x == y | x > y : #verifica que al menos una de las condiciones se cumpla para ejecutar el codigo
  print('operadores logicos ')
    
if not x > 0 : #verifica que la condicion sea lo opuesto a la condicion original
  print('operadores logicos')